    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String host = "jdbc:mysql://localhost:3306/rental_mobil",
            user = "root",
            pass = "";
            Connection con = (Connection) DriverManager.getConnection(host, user, pass);

            String No = no.getText();
            String Nama = nama.getText();
            String Nik = nik.getText();
            String Mobil = mobil.getText();
            String NoPolisi = noPolisi.getText();
            String TglPinjam = tglPinjam.getText();
            String Harga = harga.getText();
            

            Statement st = (Statement) con.createStatement();
            st.executeUpdate("UPDATE penyewa SET Nama='"+Nama+"', Nik='"+Nik+"', namaMobil='"+Mobil+"', noPolisi='"+NoPolisi+"', tglPinjam='"+TglPinjam+"', Harga='"+Harga+"'  WHERE No='"+No+"'");
        }catch(SQLException err) {
            JOptionPane.showMessageDialog(null, err.getMessage());
        }

        table();
        del();
    }//GEN-LAST:event_jButton1ActionPerformed

