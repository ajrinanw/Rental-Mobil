    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        String kodeMobil = cmbKodeMobil.getSelectedItem().toString();
        
        try {
            String host = "jdbc:mysql://localhost:3306/rental_mobil",
            user = "root",
            pass = "";
            
            Connection con = (Connection) DriverManager.getConnection(host, user, pass);
            PreparedStatement pst = con.prepareStatement("SELECT * FROM mobil WHERE kode = ?");
            pst.setString(1, kodeMobil);
            ResultSet rs = pst.executeQuery();
            
            if(rs.next()==true){
                mobil.setText(rs.getString(2) + " - " + rs.getString(3));
                noPolisi.setText(rs.getString(5));
                harga.setText(rs.getString(7));
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnOKActionPerformed

